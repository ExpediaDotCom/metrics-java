package com.expedia.metrics;

import javax.xml.bind.DatatypeConverter;
import java.nio.charset.StandardCharsets;
import java.security.MessageDigest;
import java.security.NoSuchAlgorithmException;

public class HashIdFactory implements IdFactory {

    DefaultIdFactory defaultIdFactory;

    public HashIdFactory() {
        defaultIdFactory = new DefaultIdFactory();
    }

    @Override
    public String getId(MetricDefinition metric) {
        MessageDigest md;
        try {
            md = MessageDigest.getInstance("MD5");
        } catch (NoSuchAlgorithmException e) {
            throw new RuntimeException("The JRE is missing required digest algorithm MD5", e);
        }
        md.update(defaultIdFactory.getId(metric).getBytes(StandardCharsets.UTF_8));

        byte[] digest = md.digest();
        return DatatypeConverter.printHexBinary(digest).toLowerCase();
    }
}
